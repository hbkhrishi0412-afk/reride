/**
 * Copies Poppins + Nunito Sans (latin) into public/fonts for offline Capacitor / WebView.
 * Used by reride-boot.js when remote Google Fonts are skipped in the native shell.
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const OUT_DIR = path.join(ROOT, 'public', 'fonts');
const POPPINS_DIR = path.join(ROOT, 'node_modules', '@fontsource', 'poppins', 'files');
const NUNITO_DIR = path.join(ROOT, 'node_modules', '@fontsource', 'nunito-sans', 'files');

const POPPINS_WEIGHTS = [400, 500, 600, 700, 800];
const NUNITO_WEIGHTS = [600, 700, 800];

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

function copyFont(srcDir, fileName) {
  const src = path.join(srcDir, fileName);
  if (!fs.existsSync(src)) {
    throw new Error(`Missing font file: ${src}`);
  }
  const dest = path.join(OUT_DIR, fileName);
  fs.copyFileSync(src, dest);
  return fileName;
}

function fontFaceRule(family, weight, fileName) {
  return [
    '@font-face {',
    `  font-family: '${family}';`,
    '  font-style: normal;',
    '  font-display: swap;',
    `  font-weight: ${weight};`,
    `  src: url('./${fileName}') format('woff2');`,
    '}',
  ].join('\n');
}

function main() {
  ensureDir(OUT_DIR);

  const rules = [];

  for (const weight of POPPINS_WEIGHTS) {
    const fileName = `poppins-latin-${weight}-normal.woff2`;
    copyFont(POPPINS_DIR, fileName);
    rules.push(fontFaceRule('Poppins', weight, fileName));
  }

  for (const weight of NUNITO_WEIGHTS) {
    const fileName = `nunito-sans-latin-${weight}-normal.woff2`;
    copyFont(NUNITO_DIR, fileName);
    rules.push(fontFaceRule('Nunito Sans', weight, fileName));
  }

  const css = [
    '/* Auto-generated by scripts/copy-bundled-fonts.cjs — do not edit */',
    '',
    ...rules,
    '',
    'html, body {',
    "  font-family: 'Poppins', 'PoppinsFallback', system-ui, -apple-system, 'Segoe UI', sans-serif;",
    '}',
    '',
  ].join('\n');

  fs.writeFileSync(path.join(OUT_DIR, 'reride-fonts.css'), css, 'utf8');
  console.log(`Bundled fonts copied to public/fonts (${rules.length} @font-face rules)`);
}

main();
